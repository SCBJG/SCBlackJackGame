
public class Card {
	private String suit;
	private int value;
	
	
		public Card (String cardSuit, int cardValue) {
			suit = cardSuit;
			value = cardValue;
		}
		
		
		public String getSuit() {
			return suit;
		}
		public int getValue() {
			return value;
		}
		
		
		
		
}
